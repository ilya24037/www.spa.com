            $this->output_lines[] = "**–°—Ç–∞—Ç—É—Å:** –ù–µ —É–¥–∞–ª–æ—Å—å –ø—Ä–æ–≤–µ—Ä–∏—Ç—å";
        }
        
        // –ê–Ω–∞–ª–∏–∑ —Ç–∞–±–ª–∏—Ü
        try {
            $tables = DB::select("SELECT name FROM sqlite_master WHERE type='table' AND name NOT LIKE 'sqlite_%'");
            $this->output_lines[] = "**–¢–∞–±–ª–∏—Ü:** " . count($tables);
            
            // –ü—Ä–æ–≤–µ—Ä—è–µ–º –Ω–∞–ª–∏—á–∏–µ –¥–∞–Ω–Ω—ã—Ö
            $tablesWithData = 0;
            $totalRecords = 0;
            
            foreach ($tables as $table) {
                try {
                    $count = DB::table($table->name)->count();
                    if ($count > 0) {
                        $tablesWithData++;
                        $totalRecords += $count;
                    }
                } catch (\Exception $e) {
                    // –ò–≥–Ω–æ—Ä–∏—Ä—É–µ–º
                }
            }
            
            $this->output_lines[] = "**–î–∞–Ω–Ω—ã–µ:** " . ($tablesWithData > 0 ? "{$totalRecords} –∑–∞–ø–∏—Å–µ–π –≤ {$tablesWithData} —Ç–∞–±–ª–∏—Ü–∞—Ö" : "–ë–∞–∑–∞ –ø—É—Å—Ç–∞");
            
            // –†–µ–∫–æ–º–µ–Ω–¥–∞—Ü–∏–∏
            if ($totalRecords == 0) {
                $this->output_lines[] = "";
                $this->output_lines[] = "üí° **–†–µ–∫–æ–º–µ–Ω–¥–∞—Ü–∏—è:** –°–æ–∑–¥–∞–π—Ç–µ Seeder –¥–ª—è —Ç–µ—Å—Ç–æ–≤—ã—Ö –¥–∞–Ω–Ω—ã—Ö";
            }
        } catch (\Exception $e) {
            $this->output_lines[] = "**–°—Ç–∞—Ç—É—Å –ë–î:** –ù–µ–¥–æ—Å—Ç—É–ø–Ω–∞";
        }
        
        $this->output_lines[] = "";
    }
    
