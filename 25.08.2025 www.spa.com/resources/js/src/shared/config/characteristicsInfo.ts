// Функция для получения информации о характеристике
export function getCharacteristicInfo(key: string): CharacteristicInfo | undefined {
  return serviceCharacteristics[key]
}

// Функция для проверки наличия подсказки у услуги
export function hasServiceTooltip(serviceId: string): boolean {
  return !!serviceCharacteristics[serviceId]
}

// Функция для получения списка всех услуг с подсказками
export function getAllServicesWithTooltips(): string[] {
  return Object.keys(serviceCharacteristics)
}

// Функция для получения статистики подсказок
export function getTooltipStats() {
  const totalServices = Object.keys(serviceCharacteristics).length
  return {
    totalServices,
    servicesWithTooltips: totalServices,
    coverage: '100%'
  }
}

// Функция для получения всех характеристик определенной категории
export function getCharacteristicsByCategory(category: 'massage' | 'spa' | 'fitness' | 'beauty'): CharacteristicInfo[] {
  const categoryMap: Record<string, string[]> = {
    massage: ['duration', 'experience', 'massage_type', 'pressure_level', 'aromatherapy', 'hot_stones', 'therapeutic_effect'],
    spa: ['duration', 'aromatherapy', 'relaxation_level', 'music_therapy', 'oils_type'],
    fitness: ['duration', 'for_athletes', 'specialization', 'certification'],
    beauty: ['duration', 'experience', 'certification', 'hygiene_standards']
  }
  
  const keys = categoryMap[category] || []
  return keys
    .map(key => serviceCharacteristics[key])
    .filter((v): v is CharacteristicInfo => Boolean(v))
}